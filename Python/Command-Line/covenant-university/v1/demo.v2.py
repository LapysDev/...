print("Program to calculate the Miles-per-Gallon of a car:\n")

# Initialization > (Miles Driven, Gallons of Gas Used)
miles_driven = float(input("Miles Driven: "))
gallons_of_gas_used = float(input("Gallons of Gas Used: "))

# Print
print("\nMiles per Gallon (MPG):", miles_driven / gallons_of_gas_used)
